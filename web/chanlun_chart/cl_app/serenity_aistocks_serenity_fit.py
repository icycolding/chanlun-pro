@lru_cache(maxsize=1)
def _build_fit_map() -> dict[str, dict[str, Any]]:
    xls = pd.ExcelFile(_AISTOCKS_XLSX_PATH)
    fit_map: dict[str, dict[str, Any]] = {}
    for sheet_name in xls.sheet_names:
        raw_df = pd.read_excel(_AISTOCKS_XLSX_PATH, sheet_name=sheet_name, header=None)
        rows = [[_normalize_cell_value(value) for value in row] for row in raw_df.values.tolist()]
        rows = [row for row in rows if any(cell for cell in row)]
        if not rows:
            continue
        header_index = 0
        for index, row in enumerate(rows):
            if sum(1 for cell in row if cell) >= 2:
                header_index = index
                break
        headers = [header or f"列{index + 1}" for index, header in enumerate(rows[header_index])]
        sheet_slug = _slugify_sheet_name(sheet_name)
        for row_index, values in enumerate(rows[header_index + 1 :], start=1):
            padded_values = values + [""] * (len(headers) - len(values))
            cells = {
                header: padded_values[index] if index < len(padded_values) else ""
                for index, header in enumerate(headers)
            }
            if not any(_normalize_text(value) for value in cells.values()):
                continue
            symbol = _candidate_value(cells, ("代码", "ticker", "symbol", "股票代码", "证券代码"))
            display_name = _candidate_value(cells, ("名称", "name", "公司")) or symbol
            market = _infer_market(symbol)
            normalized_code = _normalize_quote_code(market, symbol) if market else ""
            row_id = f"{sheet_slug}-{row_index}"
            profile = _resolve_profile(
                row_id=row_id,
                sheet_name=sheet_name,
                cells=cells,
                normalized_code=normalized_code,
            )
            fit_map[row_id] = _build_entry(
                row_id=row_id,
                sheet_slug=sheet_slug,
                symbol=symbol,
                display_name=display_name,
                market=market,
                normalized_code=normalized_code,
                profile=profile,
            )
    return fit_map


def get_serenity_aistock_fit_entry(row_id: str) -> dict[str, Any]:
    return dict(_build_fit_map().get(_normalize_text(row_id), {}))


def build_serenity_aistock_fit_view(
    *,
    row_id: str,
    sheet_slug: str,
    cells: dict[str, Any] | None = None,
    quote_target: dict[str, Any] | None = None,
) -> dict[str, Any]:
    entry = get_serenity_aistock_fit_entry(row_id)
    if entry:
        return entry

    resolved_cells = cells or {}
    resolved_quote_target = quote_target or {}
    symbol = _normalize_text(
        resolved_quote_target.get("code")
        or _candidate_value(resolved_cells, ("代码", "ticker", "symbol", "股票代码", "证券代码"))
    )
    display_name = _normalize_text(
        _candidate_value(resolved_cells, ("名称", "name", "公司")) or resolved_quote_target.get("name") or symbol
    )
    market = _normalize_text(resolved_quote_target.get("market")).lower() or _infer_market(symbol)
    normalized_code = _normalize_text(resolved_quote_target.get("normalized_code")) or _normalize_quote_code(market, symbol)
    profile = _resolve_profile(
        row_id=row_id,
        sheet_name=sheet_slug,
        cells=resolved_cells,
        normalized_code=normalized_code,
    )
    return _build_entry(
        row_id=row_id,
        sheet_slug=sheet_slug,
        symbol=symbol,
        display_name=display_name,
        market=market,
        normalized_code=normalized_code,
        profile=profile,
    )
