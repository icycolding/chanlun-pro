from __future__ import annotations

from typing import Any


_DEFAULT_STAGE_LABELS = ["主题识别", "客户验证", "收入放量", "利润兑现"]


def _section(
    title: str,
    summary: str,
    focus_points: list[str],
    tweet_ids: list[str] | None = None,
) -> dict[str, Any]:
    return {
        "title": title,
        "summary": summary,
        "focus_points": focus_points,
        "tweet_ids": tweet_ids or [],
    }


def _chain_node(title: str, summary: str, is_focus: bool = False) -> dict[str, Any]:
    return {
        "title": title,
        "summary": summary,
        "is_focus": is_focus,
    }


def _chain(
    title: str,
    summary: str,
    nodes: list[dict[str, Any]],
    takeaway: str,
) -> dict[str, Any]:
    return {
        "title": title,
        "summary": summary,
        "nodes": nodes,
        "takeaway": takeaway,
    }


def _progress(current_index: int, labels: list[str] | None = None) -> list[dict[str, str]]:
    labels = labels or _DEFAULT_STAGE_LABELS
    progress_items: list[dict[str, str]] = []
    for index, label in enumerate(labels):
        if index < current_index:
            status = "done"
        elif index == current_index:
            status = "current"
        else:
            status = "next"
        progress_items.append({"label": label, "status": status})
    return progress_items


def _stage(
    name: str,
    summary: str,
    signals: list[str],
    blockers: list[str],
    current_index: int,
    next_step: str,
    labels: list[str] | None = None,
) -> dict[str, Any]:
    return {
        "name": name,
        "summary": summary,
        "signals": signals,
        "blockers": blockers,
        "next_step": next_step,
        "progress": _progress(current_index, labels),
    }


def _scenario(label: str, market_cap: str, condition: str) -> dict[str, str]:
    return {
        "label": label,
        "market_cap": market_cap,
        "condition": condition,
    }


def _market_cap(
    currency: str,
    current_anchor: str,
    scenarios: list[dict[str, str]],
    note: str,
) -> dict[str, Any]:
    return {
        "currency": currency,
        "current_anchor": current_anchor,
        "scenarios": scenarios,
        "note": note,
    }


